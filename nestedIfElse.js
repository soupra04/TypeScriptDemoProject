"use strict";
var weekno = 1;
if (weekno == 1) {
    console.log("Sunday");
}
else if (weekno == 2) {
    console.log("Monday");
}
else if (weekno == 3) {
    console.log("Tuesday");
}
else if (weekno == 4) {
    console.log("Thursday");
}
else if (weekno == 5) {
    console.log("Friday");
}
else if (weekno == 6) {
    console.log("Saturday");
}
else {
    console.log("invalid weekno");
}
